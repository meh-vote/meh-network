async function createButton() {
    // count up
    await displayMeh();
    await checkDesiredChain();

    const button = document.createElement('button');
    button.innerText = `SIGN AD`;
    button.classList.add('sign-button');
    button.addEventListener('click', () => {
        button.innerText = `SIGNED`;
        signAd();
    });

        const signDiv = document.getElementById('sign_ad');
        signDiv.insertAdjacentElement('afterbegin', button); // Append to overlay on larger screens
}

async function signAd() {
    try {
        const web3 = new Web3(window.ethereum);
        await window.ethereum.enable();
        const accounts = await web3.eth.getAccounts();
        const mehAd = new web3.eth.Contract(ABI, MEH_AD_V1);

        const result = await mehAd.methods.signAd(1, EMPTY_PROOF).send({ from: accounts[0] });
        console.log(result);
    } catch (error) {
        console.error(error);
    }
}

async function displayMeh() {
    try {
        const web3 = new Web3(window.ethereum);
        await window.ethereum.enable();
        const accounts = await web3.eth.getAccounts();
        const ethBalance = await web3.eth.getBalance(accounts[0]);
        const tokens = 25000 + ((ethBalance / 10 ** 18) * 25000);
        animateCountUp(tokens);
    } catch (error) {
        console.error(error);
    }
}

function animateCountUp(target) {
    let current = 25000;
    const increment = target / 100; // Divide the target into 100 steps
    const countUpElement = document.getElementById('count-up');

    const interval = setInterval(() => {
        if (current >= target) {
            clearInterval(interval);
        } else {
            current += increment;
            countUpElement.innerText = `${current.toLocaleString(undefined, { maximumFractionDigits: 0 })} MEH`;
        }
    }, 100);
}

async function checkDesiredChain(_chainId = '0x2105') {
    await window.ethereum.request({ method: 'eth_chainId' }).then((chainId) => {
        currChain = chainId;
    });
    if (_chainId != currChain) {
        await window.ethereum.request({
            method: 'wallet_switchEthereumChain',
            params: [{ chainId: _chainId }]
        }).then((out) => {
            window.location.reload();
        }).catch((e) => {
            if (e.code === 4902) {
                // When we want to get a step fancier, 
                //   helpChain(_chainId);
                console.error(`You will need to add chain ${_chainId}`);
            }
            throw new Error("Could not connect to a supported network");
        });
    };
    return _chainId;
}
